    /**
     * Metodo que retorna verdadero o falso segun lo ingresado en el campo de email sea valido
     * @return boolean
     */
    public boolean validarEmail(){
        Pattern rfc2822 = Pattern.compile("^[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*@(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?$"
					);
        boolean validacion= true;
        if(ingresoEmail.getText().isEmpty()==true){
            validacion = false;
        }
         //Bloque de validacion para email usando clases PATTERN y MATCHER
	if(!rfc2822.matcher(ingresoEmail.getText()).matches()) {
	validacion = false;
			} 
        return validacion;
    }
