    /**
     * Metodo que retorna verdadero o false segun lo ingresado en los campos de fecha sean validos
     * @return boolean
     */
    public boolean validarFecha(){
        boolean validar = true;
        if((ingresaDia.getText().isEmpty()==false)&&PantallaInicial.isNumeric(ingresaDia.getText())==false){
            validar = false;
        }
        if(ingresaMes.getText().isEmpty()==false&&PantallaInicial.isNumeric(ingresaMes.getText())==false){
            validar = false;
        }
        if(ingresaAnio.getText().isEmpty()==false&&PantallaInicial.isNumeric(ingresaAnio.getText())==false){
            validar = false;
        }
        if((PantallaInicial.retornarNumero(ingresaDia.getText())==0)){
            validar = false;
        }else if(PantallaInicial.retornarNumero(ingresaDia.getText())<=0||PantallaInicial.retornarNumero(ingresaDia.getText())>31){
            validar = false;
        }
        if((PantallaInicial.retornarNumero(ingresaMes.getText())==0)){
            validar = false;
        }else if(PantallaInicial.retornarNumero(ingresaMes.getText())<=0||PantallaInicial.retornarNumero(ingresaMes.getText())>12){
            validar = false;
        }
        if((PantallaInicial.retornarNumero(ingresaAnio.getText())==0)){
            validar = false;
        }else if(PantallaInicial.retornarNumero(ingresaAnio.getText())<=2000||PantallaInicial.retornarNumero(ingresaAnio.getText())>2021){
            validar = false;
        }
        return validar;
    }
