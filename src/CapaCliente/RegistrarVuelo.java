    private void botonGuardarYSalirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarYSalirActionPerformed
        // TODO add your handling code here:
        if(validarIngresoCiudadAvion()&&validarIngresoDestinoAvion()&&validarIngresoNumPasajerosAvion()&&validarIngresoHoraAvion()){
            Avion avion = new Avion(Integer.parseInt(ingresoNumPasajerosVuelo.getText()), ingresoHoraLlegadaVuelo.getText(), MenuOpcionesPrincipales.numeroVueloRegistrar, ingresoCiudadProvieneVuelo.getText(), ingresoCiudadDestinoVuelo.getText());
            PantallaInicial.agregarVuelo(PantallaInicial.vuelos, avion);
            MenuOpcionesPrincipales m1 = new MenuOpcionesPrincipales();
            this.setVisible(false);
            m1.setVisible(true);
        }else if(validarIngresoCiudadAvion()==false){
            JOptionPane.showMessageDialog(null, "ERROR EN EL CAMPO DE INGRESO DE LA CIUDAD DE ORIGEN");
        }else if(validarIngresoDestinoAvion()==false){
            JOptionPane.showMessageDialog(null, "ERROR EN EL CAMPO DE INGRESO DE LA CIUDAD DE DESTINO");
        }else if(validarIngresoNumPasajerosAvion()==false){
            JOptionPane.showMessageDialog(null, "ERROR EN EL CAMPO DE INGRESO DEL NUMERO DE PASAJEROS");
        }else if(validarIngresoHoraAvion()==false){
            JOptionPane.showMessageDialog(null, "ERROR EN EL CAMPO DE INGRESO DE LA HORA DE LLEGADA");
        }else{
            JOptionPane.showMessageDialog(null, "ERROR INESPERADO");
        }
        
    }//GEN-LAST:event_botonGuardarYSalirActionPerformed

