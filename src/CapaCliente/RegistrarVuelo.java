     public boolean validarIngresoCiudadAvion(){
        boolean validacion= true;
        if(ingresoCiudadProvieneVuelo.getText().isEmpty()==true){
            validacion = false;
        }
        if(PantallaInicial.isNumeric(ingresoCiudadProvieneVuelo.getText())==true){
            validacion = false;
        }
        return validacion;
    }
     public boolean validarIngresoDestinoAvion(){
        boolean validacion= true;
        if(ingresoCiudadDestinoVuelo.getText().isEmpty()==true){
            validacion = false;
        }
        if(PantallaInicial.isNumeric(ingresoCiudadDestinoVuelo.getText())==true){
            validacion = false;
        }
        return validacion;
    }
      public boolean validarIngresoHoraAvion(){
        boolean validacion= true;
        if(ingresoHoraLlegadaVuelo.getText().isEmpty()==true){
            validacion = false;
        }
        return validacion;
    }
      public boolean validarIngresoNumPasajerosAvion(){
        boolean validacion= true;
        if((ingresoNumPasajerosVuelo.getText().isEmpty()==false)&&PantallaInicial.isNumeric(ingresoNumPasajerosVuelo.getText())==false){
            validacion = false;
        }
        return validacion;
    }
    
